// publication class
public class Publication {
	String link;
	String title;
	String authors;
	String journal;
	int year;
	
	// constructor of Publication
	public Publication(String link, String title, String authors, String journal, int year) {
		this.link = link;
		this.title = title;
		this.authors = authors;
		this.journal = journal;
		this.year = year;
	}
}